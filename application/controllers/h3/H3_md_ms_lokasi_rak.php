	public function update()
	{
		$this->validate();

		$this->db->trans_start();

		$lokasi_rak = $this->input->post(['kode_lokasi_rak', 'kapasitas', 'id_gudang', 'lokasi_retur', 'deskripsi', 'active']);
		$this->lokasi_rak->update($lokasi_rak, $this->input->post(['id']));
		$id_lokasi_rak = $this->input->post('id');

		$this->lokasi_rak_parts->delete($id_lokasi_rak, 'id_lokasi_rak');
		if ($this->input->post('parts') != null and count($this->input->post('parts')) > 0) {
			$parts = $this->getOnly(true, $this->input->post('parts'), [
				'id_lokasi_rak' => $id_lokasi_rak
			]);
			$this->lokasi_rak_parts->insert_batch($parts);
		}

		$this->db->trans_complete();

		$lokasi = (array) $this->lokasi_rak->get($this->input->post(['id']), true);
		if ($this->db->trans_status() and $lokasi != null) {
			send_json([
				'message' => 'Berhasil memperbarui lokasi rak parts',
				'payload' => $lokasi,
				'redirect_url' => base_url(sprintf('h3/%s/detail?id=%s', $this->page, $lokasi['id']))
			]);
		} else {
			send_json([
				'message' => 'Tidak berhasil memperbarui lokasi rak parts',
			], 422);
		}
	}

	public function validate()
	{
		$this->form_validation->set_error_delimiters('', '');
		if ($this->uri->segment(3) == 'update') {
			$lokasi_rak = $this->lokasi_rak->get($this->input->post(['id']), true);
			if ($lokasi_rak->kode_lokasi_rak != $this->input->post('kode_lokasi_rak')) {
				$this->form_validation->set_rules('kode_lokasi_rak', 'Kode Lokasi Rak', 'required|is_unique[ms_h3_md_lokasi_rak.kode_lokasi_rak]|max_length[30]');
			}
		} else {
			$this->form_validation->set_rules('kode_lokasi_rak', 'Kode Lokasi Rak', 'required|is_unique[ms_h3_md_lokasi_rak.kode_lokasi_rak]|max_length[30]');
		}
		$this->form_validation->set_rules('kapasitas', 'Kapasitas', 'required|numeric');
		$this->form_validation->set_rules('id_gudang', 'Gudang', 'required');
		$this->form_validation->set_rules('lokasi_retur', 'Lokasi Retur', 'required');
		$this->form_validation->set_rules('deskripsi', 'Deskripsi', 'required|max_length[100]');

		if (!$this->form_validation->run()) {
			send_json([
				'error_type' => 'validation_error',
				'message' => 'Data tidak valid',
				'errors' => $this->form_validation->error_array()
			], 422);
		}
	}

	public function sinkron_parts()
	{
		$this->db->trans_start();
		$stock = $this->db
			->select('sp.id_part')
			->select('sp.id_lokasi_rak')
			->from('tr_stok_part as sp')
			->get()->result_array();

		foreach ($stock as $row) {
			$part_sudah_terdaftar_di_lokasi = $this->db
				->from('ms_h3_md_lokasi_rak_parts as lrp')
				->where('lrp.id_lokasi_rak', $row['id_lokasi_rak'])
				->where('lrp.id_part', $row['id_part'])
				->get()->row();

			if ($part_sudah_terdaftar_di_lokasi == null) {
				$this->lokasi_rak_parts->insert([
					'id_lokasi_rak' => $row['id_lokasi_rak'],
					'id_part' => $row['id_part'],
					'qty_maks' => 1
				]);
			}
		}
		$this->db->trans_complete();

		if ($this->db->trans_status()) {
			echo "Berhasil sinkron";
		} else {
			echo "Gagal sinkron";
		}
	}

