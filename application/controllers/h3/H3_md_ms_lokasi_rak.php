	public function save()
	{
		$this->validate();

		$this->db->trans_start();
		$lokasi_rak = $this->input->post(['kode_lokasi_rak', 'kapasitas', 'id_gudang', 'lokasi_retur', 'deskripsi']);
		$this->lokasi_rak->insert($lokasi_rak);
		$id_lokasi_rak = $this->db->insert_id();
		if ($this->input->post('parts') != null and count($this->input->post('parts')) > 0) {
			$parts = $this->getOnly(true, $this->input->post('parts'), [
				'id_lokasi_rak' => $id_lokasi_rak
			]);
			$this->lokasi_rak_parts->insert_batch($parts);
		}
		$this->db->trans_complete();

		$lokasi = (array) $this->lokasi_rak->find($id_lokasi_rak);
		if ($this->db->trans_status() and $lokasi != null) {
			send_json([
				'message' => 'Berhasil menyimpan lokasi rak parts',
				'payload' => $lokasi,
				'redirect_url' => base_url(sprintf('h3/%s/detail?id=%s', $this->page, $lokasi['id']))
			]);
		} else {
			send_json([
				'message' => 'Tidak berhasil menyimpan lokasi rak parts',
			], 422);
		}
	}

	public function detail()
	{
		$data['mode']    = 'detail';
		$data['set']     = "form";
		$data['lokasi_rak'] = $this->db
			->select('l.*')
			->select('g.nama_gudang')
