	public function inject()
	{
		$lines = $this->upload_dan_baca_lokasi_rak();
		$processed_data = $this->proses_lokasi_rak($lines);

		$this->db->trans_begin();

		$all_valid = true;
		$validation_error = [];
		foreach ($processed_data as $data) {
			$this->form_validation->set_data($data);

			//Check apakah rak tersebut ada atau tidak di db 
			$check_db_master = $this->db->select('kode_lokasi_rak')
										->from('ms_h3_md_lokasi_rak')
										->where ('kode_lokasi_rak',$data['kode_lokasi_rak'])
										->get()->row_array();

			if($check_db_master['kode_lokasi_rak'] == '' || $check_db_master['kode_lokasi_rak'] == NULL){
				$data_rak = array(
					'kode_lokasi_rak' => $data['kode_lokasi_rak'],
					'deskripsi' => $data['deskripsi'],
					'kapasitas' => $data['kapasitas'],
					'kapasitas_terpakai' => $data['kapasitas_terpakai'],
					'id_gudang' => $data['id_gudang'],
					'lokasi_retur' => $data['lokasi_retur'],
					'active' => $data['active'],
				);
				
				$this->db->insert('ms_h3_md_lokasi_rak', $data_rak);
			}else{
				$this->db->set('kapasitas', $data['kapasitas']);
				$this->db->where_in('kode_lokasi_rak', $data['kode_lokasi_rak']);
				$this->db->update('ms_h3_md_lokasi_rak');
			}
		}

		if ($this->db->trans_status()) {
			$this->db->trans_commit();
			$this->session->set_userdata('pesan', 'File Lokasi Rak berhasil diupload.');
			$this->session->set_userdata('tipe', 'success');
		} else {
			$this->db->trans_rollback();
			$this->session->set_userdata('pesan', 'File Lokasi Rak tidak berhasil diupload.');
			$this->session->set_userdata('tipe', 'danger');
		}
	}

	public function upload_dan_baca_lokasi_rak()
	{
		$upload_path = "./uploads/master_lokasi_rak_header";
		$config['upload_path'] = $upload_path;
		$config['allowed_types'] = '*';
		$config['overwrite'] = true;

		$this->load->library('upload');
		$this->upload->initialize($config);

		if ($this->upload->do_upload('file')) {
			$data = $this->upload->data();
			$myfile = fopen("$upload_path/{$data['file_name']}", "r");

			$lines = [];
			while ($line = fgets($myfile)) {
				$lines[] = $line;
			}
			return $lines;
		}
	}

	public function proses_lokasi_rak($rak)
	{
		$registedInvoiceNumber = [];
		$finalData = [];

		$keys = [
			'kode_lokasi_rak', 'deskripsi','kapasitas','kapasitas_terpakai','id_gudang','lokasi_retur','active'		
		];

		foreach ($rak as $line) {
			// Lakukan pemecahan berdasarkan panjang karakter yang telah ditentukan.
			$column = $this->parsing_lokasi_rak($line);

			$index = 0;
			$subArr = [];
			foreach ($keys as $index => $key) {
				$subArr[$key] = trim($column[$index]);				
			}
			$finalData[] = $subArr;
		}

		return $finalData;
	}

	public function parsing_lokasi_rak($line)
	{
		$blocks = explode(';', $line);
		array_pop($blocks);
		return $blocks;
	}

